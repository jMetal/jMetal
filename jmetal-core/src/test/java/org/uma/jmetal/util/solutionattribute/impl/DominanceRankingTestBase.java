    private double[][] concat(double[][] a, double[][] b) {
        double[][] rv = new double[a.length + b.length][];
        for (int i = 0; i < a.length; ++i) {
            rv[i] = a[i].clone();
        }
        for (int i = 0; i < b.length; ++i) {
            rv[a.length + i] = b[i].clone();
        }
        return rv;
    }

    private int[] concat(int[] a, int[] b) {
        int[] rv = new int[a.length + b.length];
        System.arraycopy(a, 0, rv, 0, a.length);
        System.arraycopy(b, 0, rv, a.length, b.length);
        return rv;
    }

    private double[][] fill(int rows, int cols, double value) {
        double[][] rv = new double[rows][cols];
        for (double[] a : rv) {
            Arrays.fill(a, value);
        }
        return rv;
    }

    private int[] fill(int size, int value) {
        int[] rv = new int[size];
        Arrays.fill(rv, value);
        return rv;
    }

    private double[][] generateHypercube(int dim, int size) {
        if (dim == 1) {
            double[][] rv = new double[size][dim];
            for (int i = 0; i < size; ++i) {
                rv[i] = new double[] {i};
            }
            return rv;
        } else {
            double[][] rec = generateHypercube(dim - 1, size);
            double[][] rv = new double[size * rec.length][];
            for (int i = 0; i < rec.length; ++i) {
                for (int j = 0; j < size; ++j) {
                    double[] newValue = new double[dim];
                    System.arraycopy(rec[i], 0, newValue, 0, dim - 1);
                    newValue[dim - 1] = j;
                    rv[i * size + j] = newValue;
                }
            }
            return rv;
        }
    }

    private int[] hypercubeRanks(double[][] hypercube) {
        int[] rv = new int[hypercube.length];
        for (int i = 0; i < hypercube.length; ++i) {
            for (int j = 0; j < hypercube[i].length; ++j) {
                rv[i] += hypercube[i][j];
            }
        }
        return rv;
    }

    private void checkOne(double[][] points, int[] expectedRanks) {
        List<PointSolution> solutions = new ArrayList<>(points.length);
        for (int i = 0; i < points.length; i++) {
            double[] pt = points[i];
            PointSolution sol = new PointSolution(pt.length);
            for (int o = 0; o < pt.length; ++o) {
                sol.setObjective(o, pt[o]);
            }
            INDEX_ATTRIBUTE.setAttribute(sol, i);
            solutions.add(sol);
        }
        Ranking<PointSolution> ranking = makeRanking();
        ranking.computeRanking(solutions);

        // Phase 1: check whether the attributes are correctly set.
        for (int i = 0; i < points.length; ++i) {
            Assert.assertEquals(expectedRanks[i], ranking.getAttribute(solutions.get(i)).intValue());
        }

        // Phase 2: check whether fronts are consistent and contain each solution at most once
        int nFronts = ranking.getNumberOfSubFronts();
        boolean[] used = new boolean[points.length];
        int nSolutionsSeen = 0;
        for (int front = 0; front < nFronts; ++front) {
            for (PointSolution sol : ranking.getSubFront(front)) {
                Assert.assertNotNull(sol);
                Assert.assertEquals(front, ranking.getAttribute(sol).intValue());
                int index = INDEX_ATTRIBUTE.getAttribute(sol);
                Assert.assertSame(solutions.get(index), sol);
                Assert.assertFalse(used[index]);
                used[index] = true;
                ++nSolutionsSeen;
            }
        }

        // Phase 3: each solution should be exactly once in the fronts.
        Assert.assertEquals(points.length, nSolutionsSeen);
    }

    private void groupCheck(double[][] points, int[] expectedRanks) {
        int n = points.length;
        double[][] dupPoints = new double[n * 2][];
        int[] dupRanks = new int[n * 2];
        for (int i = 0; i < n; ++i) {
            dupPoints[i] = points[i].clone();
            dupPoints[i + n] = points[i].clone();
            dupRanks[i] = expectedRanks[i];
            dupRanks[i + n] = expectedRanks[i];
        }

        checkOne(points, expectedRanks);
        checkOne(dupPoints, dupRanks);
    }

